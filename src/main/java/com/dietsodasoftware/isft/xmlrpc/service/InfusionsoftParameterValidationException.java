package com.dietsodasoftware.isft.xmlrpc.service;

@SuppressWarnings("serial")
public class InfusionsoftParameterValidationException extends Exception {

	public InfusionsoftParameterValidationException() {
		super();
	}

	public InfusionsoftParameterValidationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public InfusionsoftParameterValidationException(String arg0) {
		super(arg0);
	}

	public InfusionsoftParameterValidationException(Throwable arg0) {
		super(arg0);
	}

	
}
